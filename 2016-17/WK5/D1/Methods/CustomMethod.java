public class CustomMethod {

  //Ayyyyye! It's our own method!
  public static void newLine() {
    System.out.println("1.5 Line.");
  }

  public static void main(String[] args) {
    System.out.println("First Line. ");
    newLine();
    System.out.println("Second Line. ");
  }
}
