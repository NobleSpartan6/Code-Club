class WhileLoop {
  public static void main(String[] args) {
    int x = 0;
    while(x < 10) {
      //While Loops run until parameter is false.
      // X is currently <10, so while will continue to run.
      System.out.println(x);
      x++;
      //This While Loop will print 10 numbers, counting 1-10.
    }
  }
}
