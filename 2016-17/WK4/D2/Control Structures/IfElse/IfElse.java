class IfElse {
  public static void main(String[] args) {
    //creates a boolean variable named usedBook
    boolean usedBook = true;

    // test to see if the variable usedBook is true or false
    if (usedBook == false) {
      // if the book is new, this should be executed
      System.out.println("This book is brand new");
    }
    else {
      // if the book is used, this should be executed
      System.out.println("This book is used");
    }
  }
}
