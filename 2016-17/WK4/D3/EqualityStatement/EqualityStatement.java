class EqualityStatement {
  public static void main(String[] args) {
    // == compares two values
    Boolean truefalse = 7 == 0;
    System.out.println(truefalse);
    int number = 0;
    System.out.println(number = 7); //gives number a new value and prints its new value);
  }
}
